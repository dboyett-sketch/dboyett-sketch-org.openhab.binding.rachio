    /**
     * Verify HMAC signature for webhook
     */
    public static boolean verifyHmacSignature(String payload, String signature, String secret) {
        if (payload == null || signature == null || secret == null) {
            return false;
        }
        
        try {
            Mac mac = Mac.getInstance(HMAC_SHA256);
            SecretKeySpec secretKey = new SecretKeySpec(secret.getBytes(StandardCharsets.UTF_8), HMAC_SHA256);
            mac.init(secretKey);
            
            byte[] hmacBytes = mac.doFinal(payload.getBytes(StandardCharsets.UTF_8));
            String computedSignature = bytesToHex(hmacBytes);
            
            // Constant-time comparison to prevent timing attacks
            return constantTimeEquals(computedSignature, signature);
            
        } catch (NoSuchAlgorithmException | InvalidKeyException e) {
            logger.error("Error verifying HMAC signature: {}", e.getMessage(), e);
            return false;
        }
    }
    
    /**
     * Convert bytes to hex string
     */
    private static String bytesToHex(byte[] bytes) {
        StringBuilder hexString = new StringBuilder(2 * bytes.length);
        for (byte b : bytes) {
            String hex = Integer.toHexString(0xff & b);
            if (hex.length() == 1) {
                hexString.append('0');
            }
            hexString.append(hex);
        }
        return hexString.toString();
    }
    
    /**
     * Constant-time string comparison to prevent timing attacks
     */
    private static boolean constantTimeEquals(String a, String b) {
        if (a.length() != b.length()) {
            return false;
        }
        
        int result = 0;
        for (int i = 0; i < a.length(); i++) {
            result |= a.charAt(i) ^ b.charAt(i);
        }
        return result == 0;
    }
    
    /**
     * Check if IP is in AWS IP ranges (for Rachio webhooks)
     */
    public static boolean isIpInAWSRanges(String ipAddress) {
        // Simple implementation - in production, use a proper CIDR checking library
        for (String cidr : AWS_IP_RANGES) {
            if (isIpInCidr(ipAddress, cidr)) {
                return true;
            }
        }
        return false;
    }
    
    /**
     * Check if IP is in CIDR range
     */
    private static boolean isIpInCidr(String ipAddress, String cidr) {
        try {
            String[] parts = cidr.split("/");
            String network = parts[0];
            int prefixLength = Integer.parseInt(parts[1]);
            
            long ipLong = ipToLong(ipAddress);
            long networkLong = ipToLong(network);
            long mask = ~((1L << (32 - prefixLength)) - 1);
            
            return (ipLong & mask) == (networkLong & mask);
            
        } catch (Exception e) {
            logger.debug("Error checking CIDR range: {}", e.getMessage());
            return false;
        }
    }
    
    /**
     * Convert IP address to long
     */
    private static long ipToLong(String ipAddress) {
        String[] octets = ipAddress.split("\\.");
        long result = 0;
        for (int i = 0; i < 4; i++) {
            result |= Long.parseLong(octets[i]) << (24 - (8 * i));
        }
        return result;
    }
    
    /**
     * Validate webhook URL (basic validation)
     */
    public static boolean isValidWebhookUrl(@Nullable String url) {
        if (url == null || url.trim().isEmpty()) {
            return false;
        }
        
        try {
            // Basic URL validation
            java.net.URI uri = new java.net.URI(url.trim());
            String scheme = uri.getScheme();
            String host = uri.getHost();
            
            return ("http".equals(scheme) || "https".equals(scheme)) && 
                   host != null && !host.isEmpty();
            
        } catch (Exception e) {
            logger.debug("Invalid webhook URL: {}", url);
            return false;
        }
    }
    
